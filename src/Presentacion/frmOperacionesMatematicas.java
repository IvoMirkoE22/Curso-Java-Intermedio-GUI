    private void txtSumarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtSumarMouseClicked
        double sValor1 = Double.parseDouble(txtValor1.getText());
        double sValor2 = Double.parseDouble(txtValor2.getText());
        
        double sResultado = sValor1 + sValor2;
        txtResultado.setText(String.valueOf(sResultado) );
    }//GEN-LAST:event_txtSumarMouseClicked

    private void txtRestarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtRestarMouseClicked
        double sValor1 = Double.parseDouble(txtValor1.getText());
        double sValor2 = Double.parseDouble(txtValor2.getText());
        
        double sResultado = sValor1 - sValor2;
        txtResultado.setText(String.valueOf(sResultado) );
    }//GEN-LAST:event_txtRestarMouseClicked

    private void txtMultiplicarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtMultiplicarMouseClicked
        double sValor1 = Double.parseDouble(txtValor1.getText());
        double sValor2 = Double.parseDouble(txtValor2.getText());
        
        double sResultado = sValor1 * sValor2;
        txtResultado.setText(String.valueOf(sResultado) );
    }//GEN-LAST:event_txtMultiplicarMouseClicked

